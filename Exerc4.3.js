//Escreva um while que exiba a contagem regressiva de 10 a 1.
//Ao final, exiba "Feliz Ano Novo!".

let contador = 10;

// Usando o while para contar de 10 a 1
while (contador > 0) {
    console.log(contador);
    contador--; // Decrementando o contador
}

// Exibindo a mensagem de Feliz Ano Novo
console.log("Feliz Ano Novo!");